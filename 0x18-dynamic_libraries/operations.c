#include "operations.h"

/**
 * add - function add
 * @a: param
 * @b: param
 * Return: sum
*/

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function subtract
 * @a: param
 * @b: param
 * Return: diff
*/

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - function multiply
 * @a: param
 * @b: param
 * Return: product
*/

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - function divid
 * @a: param
 * @b: param
 * Return: quotient
*/

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - function modulo
 * @a: param
 * @b: param
 * Return: rest
*/

int mod(int a, int b)
{
	return (a % b);
}
