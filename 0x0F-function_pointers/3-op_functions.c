#include "3-calc.h"

/**
 * op_add - sum of two numbers
 * @a: input
 * @b: input
 * Return: result
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub two number
 * @a: input
 * @b: input
 * Return: result
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplie two number
 * @a: input
 * @b: input
 * Return: result
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divid two number
 * @a: input
 * @b: input
 * Return: result
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module of two number
 * @a: input
 * @b: input
 * Return: result
*/

int op_mod(int a, int b)
{
	return (a % b);
}
